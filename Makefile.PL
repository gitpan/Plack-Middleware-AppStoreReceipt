  "ABSTRACT" => "The Plack::Middleware that verifies Apple App Store Receipts for In-App Purchases",
  "AUTHOR" => "zdk <zdk\@cpan.org>",
  "BUILD_REQUIRES" => {},
  "CONFIGURE_REQUIRES" => {
    "ExtUtils::MakeMaker" => "6.30"
  },
  "DISTNAME" => "Plack-Middleware-AppStoreReceipt",
  "EXE_FILES" => [],
  "LICENSE" => "perl",
  "NAME" => "Plack::Middleware::AppStoreReceipt",
  "PREREQ_PM" => {
    "AnyEvent::HTTP" => 0,
    "JSON" => 0,
    "Plack::Middleware" => 0,
    "Plack::Request" => 0,
    "Plack::Util::Accessor" => 0,
    "Try::Tiny" => 0,
    "parent" => 0,
    "strict" => 0,
    "warnings" => 0
  },
